#include <iostream>

using namespace std;

int main() {
    int num1, num2;

    cout << "1つ目の数 : ";
    cin >> num1;
    cout << "2つ目の数 : ";
    cin >> num2;

    cout << num1 << " + " << num2 << " = " << num1 + num2 << endl;
    cout << num1 << " - " << num2 << " = " << num1 - num2 << endl;

    return 0;
}
